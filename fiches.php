          <h3>Quiz</h3>
          
          <a id="vrai_faux" href="javascript:;" class="list-group-item arrow" onclick="show(37, this.id);">Vrai ou faux ?</a>
          <p id="37" class="list-group-item grey notDisplayed little">
            <strong>Le blanchiment abîme-t-il les dents ?</strong>
            <br/>
            <strong>FAUX</strong> : Les produits de blanchiment (peroxyde de carbamide ou d’hydrogène) sont de plus en plus efficaces tout en étant inoffensifs pour les tissus dentaires. Leur innocuité a été prouvée dans de nombreuses études cliniques réalisées sur plusieurs années. 
            <br/>
            L’application au Cabinet ou à domicile des agents de blanchiment doit être obligatoirement précédée d’un examen minutieux de la dentition et d’un nettoyage-polissage de la surface des dents.
            En contact avec les dents, le gel de blanchiment libère de l’oxygène qui pénètre au sein de l’émail et de la dentine provoquant leur éclaircissement.
            Une sensibilité dentinaire au chaud froid peut survenir transitoirement et être traitée en appliquant des pâtes fluorées.
            <br/>
            <br/>
            <strong>Les caries font-elles toujours mal ?</strong>
            <br/>
            <strong>FAUX</strong> : Probablement plus de la moitié des caries ne sont pas ressenties par les patients car elles ne sont pas douloureuses. D’où l’importance d’un dépistage très régulier.
            <br/>
            <br/>
            <strong>Avec l'âge, le déchaussement des dents est-il une fatalité ?</strong>
            <br/>
            <strong>FAUX</strong> : Des mesures de prévention efficaces et des traitements adaptés existent pour se prémunir de ces problèmes. Garder une bouche saine tout au long de sa vie est possible grâce à un suivi bucco-dentaire dès le plus jeune âge.
            <br/>
            <br/>
            <strong>Les infections dentaires peuvent provoquer des problèmes cardiaques !</strong>
            <br/>
            <strong>VRAI</strong> : Les microbes présents dans les infections dentaires peuvent passer dans la circulation sanguine et provoquer des problèmes cardiaques. Ceci est d’autant plus vrai chez les personnes qui présentent déjà une pathologie du cœur.
            <br/>
            <br/>
            <strong>Le tartre déchausse les dents</strong>
            <br/>
            <strong>VRAI</strong> : Le tartre est très poreux. C’est un vrai nid à microbes. S’il n’est pas retiré régulièrement, les microbes qui s’y logent peuvent provoquer une inflammation de la gencive, entraînant un déchaussement des dents.
            <br/>
            <br/>
            <strong>Pas de soins dentaires chez la femme encei</strong>nte
            <br/>
            <strong>FAUX</strong> : Des soins peuvent être prodigués pendant le deuxième trimestre de la grossesse. Au premier et au troisième trimestre, seuls les soins d'urgence seront réalisés.
            <br/>
            <br/>
            <strong>Les sodas, même sans sucre, sont mauvais pour les dents</strong>
            <br/>
            <strong>VRAI</strong> : Toutes les boissons acides (sodas, jus de fruits, jus de citron…) provoquent des déminéralisations de la surface de l'émail et diminuent ainsi la résistance des dents en face de la maladie carieuse. Leur consommation doit donc se faire avec modération.
            En cas de sensibilité forte aux caries, optez pour une paille afin que l'acidité ne se répande pas dans toute la bouche.
            <br/>
            <br/>
            <strong>Pour être plus efficace, le brossage des dents doit se faire avec une brosse à poils durs</strong>
            <br/>
            <strong>FAUX</strong> : Les brosses à poils durs usent l'émail des dents et abîment les gencives. La brosse à dents doit donc être à poils souples, ou éventuellement médium.
            <br/>
            <br/>
            <strong>Le détartrage abîme les dents</strong>
            <br/>
            <strong>FAUX</strong> : Le détartrage n’abîme pas les dents, bien au contraire il soulage les gencives. Les instruments utilisés ne rayent pas nos dents et ne les rendent pas mobiles.
            <br/>
            <br/>
            <strong>La cigarette provoque des caries</strong>
            <br/>
            <strong>VRAI</strong> : L’affaiblissement du système immunitaire augmente la concentration de bactéries cariogènes dans la salive du fumeur. Le risque de caries serait donc plus élevé chez le fumeur régulier. De plus, une baisse du flux salivaire est fréquemment observée chez le grand fumeur.
            <br/>
            <br/>
            <strong>Il faut être jeune pour pouvoir avoir des implants !</strong>
            <br/>
            <strong>FAUX</strong> : La mise en place d'implants est possible à tout âge, si les conditions de santé de la personne ne sont pas en contradiction avec la réalisation de l'acte chirurgical. Seules quelques pathologies et médicaments particuliers peuvent contre-indiquer de tels traitements.
            <br/>
            <br/>
            <strong>Les dents de lait n'ont pas de racine !</strong>
            <br/>
            <strong>FAUX</strong> : Les dents de lait ont des racines, tout comme les dents définitives. Pourtant, quand elles tombent, on ne voit jamais les racines car elles ont été résorbées par la poussée de la dent définitive qui fait son éruption juste en dessous.
            <br/>
            <br/>
            <strong>Mon enfant, n'a que des dents de lait, pas besoin d'aller chez le dentiste</strong>
            <br/>
            <strong>FAUX</strong> : Les dents de lait doivent être contrôlées et soignées au même titre que les dents définitives. L'infection d'une dent de lait peut avoir des répercussions sur la dent définitive sous-jacente. La première visite chez le dentiste doit avoir lieu aux alentours de l’âge de 4 ans.
            <br/>
            <br/>
            <strong>Une gencive qui saigne est une gencive malade</strong>
            <br/>
            <strong>VRAI</strong> : Un saignement de gencive régulier, qu'il soit spontané ou provoqué par le brossage, reflète un état inflammatoire, voire infectieux, de votre gencive, et ceci même en l'absence de douleur.
            Des soins appropriés doivent alors être mis en oeuvre rapidement pour éviter une dégradation de votre état bucco-dentaire.
            <br/>
            <br/>
            <strong>Une dent dévitalisée ne peut pas avoir de carie</strong>
            <br/>
            <strong>FAUX</strong> : Une dent dévitalisée peut présenter une carie, exactement au même titre qu'une dent vivante. Par contre, comme le nerf de la dent a été retiré lors de la dévitalisation, la carie n'est pas douloureuse, sauf lorsque l'infection se propage au delà de la dent. On est alors au stade de l'abcès dentaire qui nécessite une prise en charge rapide.
            <br/>
            <br/>
            <strong>Il faut emmener les enfants chez le dentiste à partir de 6 ans</strong>
            <br/>
            <strong>FAUX</strong> : La première visite chez le dentiste doit se faire aux alentours de 3 - 4 ans. Les parents pensent souvent, à tort, que la première visite chez le dentiste doit se faire au moment de l'éruption des premières dents définitives, c’est-à-dire vers 6 ans.
            Or les dents de lait doivent être surveillées et soignées de la même façon que les dents définitives.
            <br/>
            Avec M'T DENTS, des visites gratuites sont proposées par l’Assurance Maladie pour vos enfants de 6 ans, 9 ans, 12 ans, 15 ans et 18 ans.
            <br/>
            <br/>
            <strong>Les dents de lait n’ont pas de caries</strong>
            <br/>
            <strong>FAUX</strong> : Les dents de lait peuvent être cariées au même titre que les dents définitives. Cependant, la plupart des caries sur les dents de lait ne deviennent douloureuses qu'aux stades les plus avancés. Il est également important de noter que les caries profondes des dents de lait peuvent venir altérer les germes des dents définitives sous-jacents.
            Il est donc primordial de prévenir ces caries par un brossage efficace dès le plus jeune âge et par une visite annuelle au cabinet dès l'âge de 3 ou 4 ans.
            <br/>
            <br/>
            <strong>Le diabète augmente le déchaussement des dents</strong>
            <br/>
            <strong>VRAI</strong> : Le déchaussement des dents (ou parodontite) est aggravé et accéléré chez les patients qui présentent un diabète non équilibré.
            Chez ces patients, le traitement du déchaussement dentaire est rendu plus difficile s'il ne se fait pas en concomitance avec le traitement du diabète.
            <br/>
            <br/>
            <strong>Certaines personnes sont allergiques aux implants</strong>
            <br/>
            <strong>FAUX</strong> : Les implants sont fabriqués en alliages de titane.
            Le titane est un matériau hautement biocompatible. Aucune allergie à un implant dentaire en titane n'a jamais été rapportée.
            <br/>
            <br/>
            <strong>Les biberons de lait peuvent provoquer des caries chez les enfants</strong>
            <br/>
            <strong>VRAI</strong> : Le lait fait partie des aliments cariogènes. Il favorise l'apparition des caries. Lorsque les enfants s'endorment avec un biberon, la stagnation d’une petite quantité de lait toute la nuit dans la bouche entraîne un fort risque d'apparition de caries.
            Si l'enfant consomme un biberon de lait avant le coucher il doit se brosser les dents juste après.
            <br/>
            <br/>
            <strong>Une gencive qui saigne est une gencive malade ?</strong>
            <br/>
            <strong>VRAI</strong> : Un saignement des gencives au brossage est le signe d'une gingivite, ou plus grave, d'une parodontite (déchaussement des dents). Le saignement est dû à une inflammation gingivale provoquée le plus souvent par la présence de bactéries et de tartre sous la gencive.
            Un traitement approprié doit alors être mis en place afin de contrôler cette situation.
            <br/>
            <br/>
            <strong>Les adultes peuvent bénéficier de traitements orthodontiques comme les enfants et les adolescents</strong>
            <br/>
            <strong>VRAI</strong> : Les déplacements et alignements dentaires peuvent être réalisés à tout âge, à condition que la bouche ait été débarrassée au préalable de tous foyers infectieux (carie, abcès, gingivite, ...).
            Chez les adultes on a recours, lorsque cela est possible, à des techniques de traitement invisibles.
            <br/>
            <br/>
            <strong>Un implant dure toute une vie</strong>
            <br/>
            <strong>FAUX</strong> : un implant peut durer de nombreuses années voire parfois toute une vie. Néanmoins, diverses complications peuvent survenir au niveau d'un implant : déchaussement, infection, fracture d'une vis ou de l'implant, ...
            Pour garantir un succès dans le temps de vos implants, un certain nombre de précautions s'imposent : hygiène irréprochable, contrôles réguliers chez votre chirurgien dentiste et utilisation d'un système d'implant éprouvé.
            Les implants sont des dispositifs efficaces et confortables mais nécessitent d'être entretenus minutieusement pour garantir leur succès à long terme.
          </p>
