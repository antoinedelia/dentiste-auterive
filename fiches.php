        <div class="list-group">
          <h3>Les choix esthétiques</h3>
          
          <a id="blanchiment" href="javascript:;" class="list-group-item arrow" onclick="show(33, this.id);">Le blanchiment</a>
          <p id="33" class="list-group-item grey notDisplayed little">
            <strong>La consultation préliminaire</strong>
            <br/>
            En consultation, le praticien et le patient décident de l’intérêt d’un éclaircissement. Un bilan bucco-dentaire préalable est effectué afin d’écarter certaines contre-indications comme la présence de caries, d’obturations non étanches ou d’inflammations gingivales. Ensuite un détartrage et polissage des arcades dentaires est indispensable avant la réalisation du traitement de blanchiment. 
            <br/>
            Il faut savoir qu’il s’agit plutôt d’un éclaircissement de la teinte naturelle de la dent et que les cas sévères de dyschromie (atteinte profonde de la teinte de l’émail liée souvent à la prise de médicaments lors de la formation de la dent) seront souvent traités par la technique des facettes. 
            <br/>
            <br/>
            <strong>Les différentes techniques d'éclaircissement :</strong>
            <br/>
            - <strong>Au cabinet :</strong> 1 à 2 séances de 30 à 60 minutes sont à prévoir au fauteuil. Après avoir mis en place une protection au niveau des gencives, un gel blanchissant est appliqué sur l’émail des dents et activé par une lampe à UV.
            <br/>
            - <strong>A la maison (technique ambulatoire) :</strong> l’empreinte des deux mâchoires est effectuée au cabinet afin d’élaborer des gouttières en plastique ajustées à la dentition.
            Le praticien les remet au patient avec un coffret contenant des seringues de gel blanchissant. Le patient appliquera le gel actif à l’intérieur des gouttières pour ensuite les insérer en bouche pendant 30 à 60 minutes par jour. La durée de la « technique ambulatoire » est en général de 15 jours. A noter que dès le deuxième jour, on obtient un résultat.
            L’avantage du blanchiment ambulatoire est, qu’ultérieurement, le patient pourra utiliser ses gouttières 6 mois après, suite à une séance de détartrage, pour redonner un coup d’éclat à ses dents.
            <br/>
            - <strong>Pour les dents dévitalisées</strong> qui ont noirci au cours du temps mais qui ne sont pas trop atteintes, il existe une technique de blanchiment spécifique. Après avoir protégé l’intérieur de la racine par une sorte de bouchon, un gel blanchissant est placé dans le reste de la partie supérieure de la dent. Le produit est laissé à l’intérieur de la dent, de quelques jours à quelques semaines. Quand la dent a récupéré la même teinte que les dents voisines, le gel est retiré et la dent est rebouchée définitivement.
          </p>
          
          <a id="coloration" href="javascript:;" class="list-group-item arrow" onclick="show(34, this.id);">Les colorations dentaires</a>
          <p id="34" class="list-group-item grey notDisplayed little">
            <strong>Mes dents sont tachées, que faire ?</strong>
            <br/>
            Les colorants alimentaires (café, thé, vin...), la nicotine ainsi que certains produits pharmaceutiques (bain de bouche à base de chlorhexidine, médication à base de fer,...) colorent très fréquemment le tartre, les dents et les restaurations dentaires à base de résine. Cela entraîne un aspect très disgracieux de la denture.
            <br/>
            Une hygiène bucco-dentaire quotidienne, des détartrages réguliers associés à un polissage des dents permettent en général de remédier à ce problème.
            <br/>
            Il est déconseillé par contre d’utiliser un dentifrice abrasif qui risquerait de rayer la surface des dents et d’accélérer indirectement l’apparition de nouvelles colorations.
            <br/>
            L’élimination de la substance colorante dans vos habitudes de vie représente toutefois la meilleure solution.
            <br/>
            Si ces taches correspondent à des petits défauts de l’émail, votre chirurgien-dentiste pourra vous proposer la technique de la micro abrasion, technique qui permet de gommer les taches en surface.
            <br/>
            <br/>
            <strong>Je ne veux pas d’amalgame, peut-on me proposer autre chose de plus esthétique ?</strong>
            <br/>
            Dans certains cas, il est possible de remplacer les amalgames par des résines composites. Cependant différentes techniques sont à envisager en fonction de l’importance de la carie.
            <br/>
            Si celle-ci est de petit volume, votre praticien pourra réaliser directement son composite pendant la séance.
            <br/>
            En revanche, pour les caries volumineuses fragilisant la dent, il est alors préférable de fabriquer un composite renforcé à partir d’une empreinte de la cavité (inlay ou onlay).
            <br/>
            <br/>
            <strong>J’ai une dent dévitalisée qui noircit. Y a-t-il une solution pour l’éclaircir ?</strong>
            <br/>
            Si la dent n’est pas trop délabrée votre chirurgien-dentiste pourra vous proposer un blanchiment interne pour l’éclaircir.
            <br/>
            Le blanchiment est une technique qui permet d’éclaircir la couleur interne des dents afin de leur redonner un éclat et une brillance plus attractives.
            <br/>
            Cette technique d’éclaircissement permet de redonner aux dents plus d’éclat et de brillance.
          </p>
        </div>

        <div class="list-group">
          <h3>Prendre soin de ses dents</h3>
          
          <a id="regles_or" href="javascript:;" class="list-group-item arrow" onclick="show(35, this.id);">Les 10 règles d'or d'une bonne hygiène dentaire</a>
          <p id="35" class="list-group-item grey notDisplayed little">
             - <strong>Idéalement deux brossages par jour sont recommandés.</strong> En pratique, deux brossages quotidiens minutieux permettent d’obtenir une hygiène satisfaisante, si la gencive est en bonne santé. En cas de maladie des gencives, les trois brossages quotidiens sont indispensables. Ne rien manger ni boire (à part de l’eau) après le brossage du soir.
            <br/>
            <br/>
             - <strong>La brosse utilisée doit être à poils souples, avec une tête de petite dimension pour accéder à tous les recoins.</strong> Des poils trop durs peuvent user votre émail de façon importante sur le long terme. Mieux vaut un brossage doux et un peu plus long qu’un brossage agressif qui risquerait d’abîmer votre émail et vos gencives.
            <br/>
            <br/>
             - <strong>La brosse manuelle permet d’obtenir d’excellents résultats lorsqu’elle est bien utilisée.</strong> Certaines brosses électriques permettent d’obtenir les mêmes résultats avec un peu plus de facilité.
            <br/>
            <br/>
             - <strong>Brossez toujours vos dents en faisant un mouvement de la gencive vers la dent.</strong> Au début du mouvement, les poils de la brosse doivent être posés sur la gencive. Attention à bien brosser toutes les faces des dents.
            <br/>
            <br/>
             - <strong>Les brossettes interdentaires et le fil dentaire font partie intégrante du brossage.</strong> En effet, la brosse à dents ne nettoie pas entre les dents. Une grande partie des problèmes dentaires (caries, déchaussements...) démarre entre les dents puisque les bactéries y stagnent. Si la gencive est en bonne santé, le passage des brossettes doit être fait une fois par jour, idéalement le soir. En cas de maladie des gencives, il peut être nécessaire de le faire 2 à 3 fois par jour. Si les espaces interdentaires sont trop étroits, les brossettes doivent être remplacées par du fil dentaire.
            <br/>
            <br/>
             - <strong>Pensez également à brosser la langue.</strong> Il existe des instruments spéciaux pour cela, mais la brosse à dents est suffisante. En effet la face externe de la langue est recouverte de bactéries qui peuvent contribuer, notamment, à une mauvaise haleine.
            <br/>
            <br/>
             - <strong>Les hydropulseurs peuvent être utilisés en complément de la brosse.</strong> Ils peuvent être utiles pour enlever des débris alimentaires et faire un massage des gencives. Cependant leur action est insuffisante pour décoller les bactéries de la plaque dentaire, et ils ne remplacent en aucune façon la brosse à dent, ni les brossettes interdentaires.
            <br/>
            <br/>
             - <strong>Le dentifrice doit être fluoré</strong> pour vous protéger des caries. A chaque âge correspond un dosage en fluor. Dans certains cas, un dentifrice spécifique peut être conseillé.
            <br/>
            <br/>
             - <strong>Les bains de bouche antiseptiques ne doivent pas être utilisés quotidiennement</strong> en dehors des cures prescrites par le dentiste.
            <br/>
            <br/>
             - <strong>Certains aliments sucrés et acides (comme les sodas ou le citron) abîment fortement les dents.</strong> Les consommer avec modération.
          </p>
          
          <a id="manuelle_electrique" href="javascript:;" class="list-group-item arrow" onclick="show(36, this.id);">Brosse à dents manuelle ou brosse à dents électrique ?</a>
          <p id="36" class="list-group-item grey notDisplayed little">
            Comme la plaque commence à se former entre 4 à 12 heures après le brossage, il est important de se brosser les dents au moins deux fois par jour.
            <br/>
            <br/>
            <strong>Quel type de brosse choisir ?</strong>
            <br/>
            Bien choisir sa brosse à dents est primordial pour l’hygiène buccale et si l’on utilise une mauvaise brosse à dents cela peut endommager les gencives.
            <br/>
            Plusieurs critères sont à prendre en compte avant l’achat d’une brosse à dents, notamment les types de poils de la brosse. Nous vous conseillerons en fonction de votre cas personnel.
            <br/>
            En général, les brosses à dents souples sont faites pour les personnes n’ayant aucun problème buccal, les brosses à dents à poils très souples sont plus adaptées aux gencives fragiles.
            <br/>
            <strong>La brosse à dents manuelle :</strong>
            <br/>
             - Est facile à transporter en cas de voyage, au bureau, à la cantine…
            <br/>
             - Présente de nombreux modèles et gammes de prix
            <br/>
             - Permet un contact plus « sensible » avec les dents
            <br/>
             - Demande de se brosser les dents 2 minutes
            <br/>
             - Changer de brosse tous les 2 ou 3 mois
            <br/>
            <br/>
            <strong>La brosse à dents électrique :</strong>
            <br/>
             - Elimine de manière approfondie la plaque dentaire
            <br/>
             - Limite l’apparition de caries ou de gingivite
            <br/>
             - Offre des fonctions complémentaires : minuteur, détecteur de pression, choix entre différentes têtes et différents modes de brossage
            <br/>
             - Choix possible entre le modèle oscillo-rotatif (petite tête de brosse qui effectue des mouvements dans un sens puis dans l’autre) et le modèle sonique (tête de brosse qui ressemble à une brosse à dents manuelle avec mouvement latéral et des vibrations)
            <br/>
             - Permet d’atteindre les endroits difficiles d’accès
            <br/>
             - Deux minutes de brossage
            <br/>
            <strong>Changer la tête de sa brosse à dents électrique tous les 3 mois</strong>
            <br/>
            <br/>
            <strong>Technique de brossage</strong>
            <br/>
             - Brosser ses dents sur de petites surfaces à la fois, avec un mouvement de rouleau
            <br/>
             - Se brosser les dents en allant des gencives vers les dents, du rose vers le blanc<br/>
            <br/>
             - Nettoyer avec le même soin les gencives, la langue et le palais
            <br/>
             - Le brossage doit être doux et méthodique pour nettoyer toutes les surfaces dentaires
            <br/>
            <br/>
            En effet, un brossage trop énergique peut abimer le tissu gingival et entraîner son déchaussement. 
            Autres conséquences : sensibilité dentaire par une mise à nue de la dentine.
          </p>
